let operator = '+'; 
let num1 = 10, num2 = 5;
switch(operator) {
    case '+': console.log(num1 + num2); break;
    case '-': console.log(num1 - num2); break;
    case '*': console.log(num1 * num2); break;
    case '/': console.log(num1 / num2); break;
    default: console.log("Invalid operator");
}
